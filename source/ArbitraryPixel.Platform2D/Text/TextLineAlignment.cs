namespace ArbitraryPixel.Platform2D.Text
{
    /// <summary>
    /// The alignment for a line of text.
    /// </summary>
    public enum TextLineAlignment
    {
        /// <summary>
        /// Align to the left edge of text bounds.
        /// </summary>
        Left,

        /// <summary>
        /// Align to the centre of text bounds.
        /// </summary>
        Centre,

        /// <summary>
        /// Align to the right of text bounds.
        /// </summary>
        Right
    }
}
